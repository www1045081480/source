package com.np.order.objects;

import java.util.List;

public class ItemNames {
	private List<String> itemCnNames;
	private List<String> itemJpNames;
	private List<String> itemEnNames;

	public List<String> getItemCnNames() {
		return itemCnNames;
	}

	public void setItemCnNames(List<String> itemCnNames) {
		this.itemCnNames = itemCnNames;
	}

	public List<String> getItemJpNames() {
		return itemJpNames;
	}

	public void setItemJpNames(List<String> itemJpNames) {
		this.itemJpNames = itemJpNames;
	}

	public List<String> getItemEnNames() {
		return itemEnNames;
	}

	public void setItemEnNames(List<String> itemEnNames) {
		this.itemEnNames = itemEnNames;
	}

}
