// JavaScript Document
function winOpen(url,sizew,sizeh){
	window.open(url,'searchWin','width='+ sizew + ',height=' + sizeh + ',menubar=no,toolbar=no,scrollbars=yes,resize=no');
}

function winOpenResizable(url,sizew,sizeh) {
  window.open(url,'searchWin','width='+ sizew + ',height=' + sizeh + ',menubar=no,toolbar=no,scrollbars=yes,resizable=yes');
}